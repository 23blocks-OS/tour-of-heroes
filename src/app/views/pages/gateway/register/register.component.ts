// Angular
import {
  ChangeDetectorRef,
  Component,
  OnDestroy,
  OnInit,
  ViewEncapsulation,
} from '@angular/core';
import { Router } from '@angular/router';
import { FormBuilder, FormGroup, Validators } from '@angular/forms';
// RxJS
import { finalize, takeUntil, tap } from 'rxjs/operators';
// Translate
// NGRX
import { Store } from '@ngrx/store';
import { AppState } from 'src/app/core/reducers';
// Auth
import {
  GatewayService,
  AuthNoticeService,
  User,
} from 'src/app/core/23blocks/gateway';
import { Subject } from 'rxjs';
import { ConfirmPasswordValidator } from './confirm-password.validator';
import { environment } from 'src/environments/environment';
@Component({
  selector: 'app-gateway-register',
  templateUrl: './register.component.html',
  encapsulation: ViewEncapsulation.None,
  styleUrls: ['./register.component.scss'],
})
export class RegisterComponent implements OnInit, OnDestroy {
  registerForm: FormGroup;
  loading = false;
  errors: any = [];
  private unsubscribe: Subject<any>; // Read more: => https://brianflove.com/2016/12/11/anguar-2-unsubscribe-observables/
  /**
   * Component constructor
   *
   * @param authNoticeService: AuthNoticeService
   * @param translate: TranslateService
   * @param router: Router
   * @param auth: AuthService
   * @param store: Store<AppState>
   * @param fb: FormBuilder
   * @param cdr
   */
  constructor(
    private authNoticeService: AuthNoticeService,
    //private translate: TranslateService,
    private router: Router,
    private auth: GatewayService,
    private store: Store<AppState>,
    private fb: FormBuilder,
    private cdr: ChangeDetectorRef
  ) {
    this.unsubscribe = new Subject();
  }
  /*
   * @ Lifecycle sequences => https://angular.io/guide/lifecycle-hooks
   */
  /**
   * On init
   */
  ngOnInit() {
    this.initRegisterForm();
  }
  /*
   * On destroy
   */
  ngOnDestroy(): void {
    this.unsubscribe.next();
    this.unsubscribe.complete();
    this.loading = false;
  }
  /**
   * Form initalization
   * Default params, validators
   */
  initRegisterForm() {
    this.registerForm = this.fb.group(
      {
        fullname: [
          '',
          Validators.compose([
            Validators.required,
            Validators.minLength(3),
            Validators.maxLength(100),
          ]),
        ],
        email: [
          '',
          Validators.compose([
            Validators.required,
            Validators.email,
            Validators.minLength(3),
            // https://stackoverflow.com/questions/386294/what-is-the-maximum-length-of-a-valid-email-address
            Validators.maxLength(320),
          ]),
        ],
        password: [
          '',
          Validators.compose([
            Validators.required,
            Validators.minLength(3),
            Validators.maxLength(100),
          ]),
        ],
        confirmPassword: [
          '',
          Validators.compose([
            Validators.required,
            Validators.minLength(3),
            Validators.maxLength(100),
          ]),
        ],
        // agree: [false, Validators.compose([Validators.required])]
      },
      {
        validator: ConfirmPasswordValidator.MatchPassword,
      }
    );
  }
  /**
   * Form Submit
   */
  submit() {
    const controls = this.registerForm.controls;
    // check form
    if (this.registerForm.invalid) {
      Object.keys(controls).forEach((controlName) =>
        controls[controlName].markAsTouched()
      );
      return;
    }
    this.loading = true;
    // if (!controls.agree.value) {
    // 	// you must agree the terms and condition
    // 	// checkbox cannot work inside mat-form-field https://github.com/angular/material2/issues/7891
    // 	this.authNoticeService.setNotice('You must agree the terms and condition', 'danger');
    // 	this.loading = false;
    // 	return;
    // }
    this.auth
      .registerUser({
        user: {
          provider: 'email',
          uid: controls.email.value,
          email: controls.email.value,
          password: controls.password.value,
          username: controls.email.value,
          name: controls.fullname.value,
        },
        subscription: '318f1533-8dd9-4a7d-8b36-9b04a7c2363f',
        confirm_success_url: environment.APP_URL + '/auth/step2',
      })
      .pipe(
        tap(),
        takeUntil(this.unsubscribe),
        finalize(() => {
          this.loading = false;
          this.cdr.markForCheck();
        })
      )
      .subscribe();
  }
  /**
   * Checking control validation
   *
   * @param controlName: string => Equals to formControlName
   * @param validationType: string => Equals to valitors name
   */
  isControlHasError(controlName: string, validationType: string): boolean {
    const control = this.registerForm.controls[controlName];
    if (!control) {
      return false;
    }
    const result =
      control.hasError(validationType) && (control.dirty || control.touched);
    return result;
  }

  onSocialLogin(user: User) {
    console.log(user);
  }
}
